void main() {
  // Membuat variabel tanpa mendeklarasikan tipe data
  var greetings = 'Hello Dart!'; // string
  print(greetings);

  var myAge = 21; // int
  print(myAge);

  var age; // dinamyc -> dapat diisi dengan tipe data apapun
  age = 20;
  print(age);
  age = '21 tahun';
  print(age);

  // Membuat variabel dengan mendeklarasikan tipe data
  String ucapan = 'Halo Dart!';
  print(ucapan);

  int usiaku = 21;
  print(usiaku);

  int usia;
  usia = 20;
  print(usia);
}
